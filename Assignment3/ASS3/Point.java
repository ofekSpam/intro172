/*
* reprsents a point in the plane.
*/
public class Point { //

	//A constructor
	public Point(int x, int y) {
	    this.x = x;
	    this.y = y;
		
	}
	
	//Returns X-axis Value of the point.
	public int getX() {
	    return this.x;
		
	}
	
	//Returns Y-axis Value of the point.
	public int getY() {
	    return this.y;
		
	}
}
